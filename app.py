# --- 4. CALCULATION ENGINE (PROXIMITY LOGIC) ---
def calculate_universe():
    # 2026 Digital Universe projection: 958M Active Monthly Users
    BASE_INDIA_DIGITAL = 958000 
    
    # Relative State Weights (Based on digital density indices)
    # This logic sums the contribution of selected states
    state_contribution = len(sel_states) / len(INDIA_GEO_MASTER)
    
    # Classification Multiplier (IAMAI 2025 proportions)
    if market_type == "Urban": class_mult = 0.43
    elif market_type == "Rural": class_mult = 0.57
    else: class_mult = 1.0 # Overall
    
    # Granularity Multiplier (If specific districts are chosen in Overall mode)
    granularity = 1.0
    if market_type == "Overall" and len(sel_districts) > 0:
        total_possible = len(district_options)
        granularity = len(sel_districts) / total_possible
        
    # Demographic Weighting
    age_weight = len(sel_age) * 0.25
    nccs_weight = len(sel_nccs) * 0.20
    gender_weight = 0.54 if "Male" in sel_gender and len(sel_gender) == 1 else (0.46 if "Female" in sel_gender and len(sel_gender) == 1 else 1.0)

    final_u = int(BASE_INDIA_DIGITAL * state_contribution * class_mult * granularity * age_weight * nccs_weight * gender_weight)
    return max(1, final_u)

